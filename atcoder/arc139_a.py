import sys
input = sys.stdin.buffer.readline
# sys.setrecursionlimit(10 ** 7)

N = int(input())
T = list(map(int, (input().split())))
ans = 0
for i in range(N):
    tmp = 1<<T[i]
    ans = (ans//tmp + 1) * tmp
    if ans%(tmp*2)==0:
        ans += tmp
print(ans)

