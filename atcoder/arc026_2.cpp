int main() {
	ll n;
	cin >> n;
    ll sum = 0;
    ll i = 1;
    while (i*i<=n) {
        if (n%i==0) {
            sum += i;
            if (i*i!=n) sum += n/i;
        }
        ++i;
    }
    if (sum > 2*n)       cout << "Abundant" << endl;
    else if (sum == 2*n) cout << "Perfect" << endl;
    else                 cout << "Deficient" << endl;
	return 0;
}
